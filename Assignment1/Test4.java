class Test4
{
public static void main(String[] args)
{ 
 // Integer literals in different number systems
        int binaryNum = 0b1010;       // Binary (prefix 0b or 0B)
        int octalNum = 012;           // Octal (prefix 0)
        int hexNum = 0xA;             // Hexadecimal (prefix 0x or 0X)

        // Floating-point literals
        double floatNum1 = 12.34;      // Decimal notation

        // Displaying values
        System.out.println("Binary 0b1010 = " + binaryNum);   // 10 in decimal
        System.out.println("Octal 012 = " + octalNum);       // 10 in decimal
        System.out.println("Hexadecimal 0xA = " + hexNum);   // 10 in decimal
        System.out.println("Floating-point 12.34 = " + floatNum1);
       }
	   }